package all.model;

public class Driver {
    String position;
    int number;
    String name;
    String team;
    int drivenLaps;
    String lose;
    int points;

    public Driver(String position, int number, String name, String team, int drivenLaps, String lose, int points) {
        this.position = position;
        this.number = number;
        this.name = name;
        this.team = team;
        this.drivenLaps = drivenLaps;
        this.lose = lose;
        this.points = points;
    }
    
}
